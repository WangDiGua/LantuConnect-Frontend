let nextId = 200;

function matchSkills(params: Record<string, string | undefined>): Skill[] {
  let result = [...skills];
  const keyword = params.keyword?.toLowerCase();
  if (keyword) {
    result = result.filter(
      (s) =>
        s.displayName.toLowerCase().includes(keyword) ||
        s.agentName.toLowerCase().includes(keyword) ||
        s.description.toLowerCase().includes(keyword),
    );
  }
  if (params.status) result = result.filter((s) => s.status === params.status);
  if (params.sourceType) result = result.filter((s) => s.sourceType === params.sourceType);
  if (params.parentId !== undefined && params.parentId !== '') {
    const pid = Number(params.parentId);
    result = result.filter((s) => s.parentId === (isNaN(pid) ? null : pid));
  }
  if (params.categoryId) result = result.filter((s) => s.categoryId === Number(params.categoryId));
  return result;
}

export function registerHandlers(mock: MockAdapter): void {
  mock.onGet('/api/v1/skills').reply((config) => {
    const p = config.params || {};
    const filtered = matchSkills(p);
    return paginate(filtered, Number(p.page) || 1, Number(p.pageSize) || 20);
  });

  mock.onGet(/\/api\/v1\/skills\/(\d+)$/).reply((config) => {
    const id = Number(config.url!.match(/\/api\/v1\/skills\/(\d+)/)?.[1]);
    const skill = skills.find((s) => s.id === id);
    return skill
      ? mockOk(skill)
      : [404, { code: 404, message: 'Skill not found', timestamp: Date.now() }];
  });

  mock.onPost('/api/v1/skills').reply((config) => {
    const body = JSON.parse(config.data || '{}');
    const parentServer = body.parentId ? mcpServers.find((m) => m.id === body.parentId) : null;
    const newSkill: Skill = {
      id: nextId++,
      agentName: body.agentName || 'new_skill',
      displayName: body.displayName || '新 Skill',
      description: body.description || '',
      agentType: body.agentType || 'mcp',
      mode: 'TOOL',
      parentId: body.parentId ?? null,
      parentName: parentServer?.displayName,
      sourceType: body.sourceType || 'internal',
      providerId: body.providerId ?? null,
      categoryId: body.categoryId ?? null,
      status: 'draft',
      displayTemplate: body.displayTemplate ?? null,
      specJson: body.specJson || {},
      parametersSchema: body.parametersSchema ?? null,
      isPublic: body.isPublic ?? false,
      icon: body.icon ?? null,
      sortOrder: skills.length + 1,
      maxConcurrency: body.maxConcurrency ?? 10,
      qualityScore: 0,
      avgLatencyMs: 0,
      successRate: 0,
      avgTokenCost: 0,
      callCount: 0,
      createTime: new Date().toISOString(),
      updateTime: new Date().toISOString(),
    };
    skills.unshift(newSkill);
    if (parentServer) parentServer.skillCount++;
    return mockOk(newSkill);
  });

  mock.onPut(/\/api\/v1\/skills\/(\d+)$/).reply((config) => {
    const id = Number(config.url!.match(/\/api\/v1\/skills\/(\d+)/)?.[1]);
    const body = JSON.parse(config.data || '{}');
    const idx = skills.findIndex((s) => s.id === id);
    if (idx >= 0) {
      skills[idx] = { ...skills[idx], ...body, updateTime: new Date().toISOString() };
      return mockOk(skills[idx]);
    }
    return [404, { code: 404, message: 'Skill not found', timestamp: Date.now() }];
  });

  mock.onDelete(/\/api\/v1\/skills\/(\d+)$/).reply((config) => {
    const id = Number(config.url!.match(/\/api\/v1\/skills\/(\d+)/)?.[1]);
    const idx = skills.findIndex((s) => s.id === id);
    if (idx >= 0) {
      const removed = skills.splice(idx, 1)[0];
      if (removed.parentId) {
        const srv = mcpServers.find((m) => m.id === removed.parentId);
        if (srv) srv.skillCount = Math.max(0, srv.skillCount - 1);
      }
    }
    return mockOk(null);
  });

  mock.onGet('/api/v1/mcp-servers').reply(() => {
    return mockOk(mcpServers);
  });
}
