let nextId = 100;

export function registerHandlers(mock: MockAdapter): void {
  mock.onGet('/agents').reply((config) => {
    const p = config.params || {};
    let filtered = [...agents];

    if (p.keyword) {
      const kw = String(p.keyword).toLowerCase();
      filtered = filtered.filter(
        (a) =>
          a.displayName.toLowerCase().includes(kw) ||
          a.agentName.toLowerCase().includes(kw) ||
          a.description.toLowerCase().includes(kw),
      );
    }
    if (p.status) filtered = filtered.filter((a) => a.status === p.status);
    if (p.sourceType) filtered = filtered.filter((a) => a.sourceType === p.sourceType);
    if (p.agentType) filtered = filtered.filter((a) => a.agentType === p.agentType);

    return paginate(filtered, Number(p.page) || 1, Number(p.pageSize) || 20);
  });

  mock.onGet(/\/agents\/(\d+)$/).reply((config) => {
    const id = Number(config.url!.match(/\/agents\/(\d+)$/)?.[1]);
    const agent = agents.find((a) => a.id === id);
    return agent
      ? mockOk(agent)
      : [404, { code: 404, message: 'Agent not found', timestamp: Date.now() }];
  });

  mock.onPost('/agents').reply((config) => {
    const body = JSON.parse(config.data || '{}');
    const now = new Date().toISOString();
    const newAgent: Agent = {
      id: nextId++,
      agentName: body.agentName || 'new_agent',
      displayName: body.displayName || '新建 Agent',
      description: body.description || '',
      agentType: body.agentType || 'mcp',
      mode: 'SUBAGENT',
      sourceType: body.sourceType || 'internal',
      providerId: body.providerId ?? null,
      categoryId: body.categoryId ?? null,
      status: 'draft',
      specJson: body.specJson || {},
      isPublic: body.isPublic ?? false,
      icon: body.icon ?? null,
      sortOrder: agents.length * 10,
      hidden: false,
      maxConcurrency: body.maxConcurrency ?? 10,
      maxSteps: body.maxSteps ?? null,
      temperature: body.temperature ?? null,
      systemPrompt: body.systemPrompt ?? null,
      qualityScore: 0,
      avgLatencyMs: 0,
      successRate: 0,
      avgTokenCost: 0,
      callCount: 0,
      createTime: now,
      updateTime: now,
    };
    agents.unshift(newAgent);
    return mockOk(newAgent);
  });

  mock.onPut(/\/agents\/(\d+)$/).reply((config) => {
    const id = Number(config.url!.match(/\/agents\/(\d+)$/)?.[1]);
    const body = JSON.parse(config.data || '{}');
    const idx = agents.findIndex((a) => a.id === id);
    if (idx >= 0) {
      agents[idx] = { ...agents[idx], ...body, updateTime: new Date().toISOString() };
      return mockOk(agents[idx]);
    }
    return [404, { code: 404, message: 'Agent not found', timestamp: Date.now() }];
  });

  mock.onDelete(/\/agents\/(\d+)$/).reply((config) => {
    const id = Number(config.url!.match(/\/agents\/(\d+)$/)?.[1]);
    const idx = agents.findIndex((a) => a.id === id);
    if (idx >= 0) agents.splice(idx, 1);
    return mockOk(null);
  });
}
